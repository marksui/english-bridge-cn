const escapeSvg = (value: string) =>
  value.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;").replace(/"/g, "&quot;");

const shortLabel = (value: string, maxLength: number) =>
  value.length > maxLength ? `${value.slice(0, maxLength - 3)}...` : value;

const fontSizeFor = (value: string, base: number) => {
  if (value.length > 34) return Math.max(24, base - 18);
  if (value.length > 24) return Math.max(28, base - 12);
  if (value.length > 16) return Math.max(32, base - 8);
  return base;
};

const imageKey = (value: string) => value.toLowerCase();

const configuredImageSourceFor = (
  categoryId: string,
  title: string,
  isCategory: boolean,
): ImageSourceConfig => {
  if (isCategory) {
    return categoryImageSources[categoryId] ?? { mode: "fallbackOnly" };
  }

  const key = imageKey(title);
  const wikiPage = wikiPageWordImages[categoryId]?.[key];
  if (wikiPage) {
    return { mode: "wikiPage", pageTitle: wikiPage };
  }

  const directUrl = directUrlWordImages[categoryId]?.[key];
  if (directUrl) {
    return { mode: "directUrl", url: directUrl };
  }

  const fallbackWords = fallbackOnlyWordImages[categoryId] ?? [];
  if (fallbackWords.some((word) => imageKey(word) === key)) {
    return { mode: "fallbackOnly" };
  }

  return { mode: "fallbackOnly" };
};

const imageUrl = (
  categoryId: string,
  title: string,
  subtitle: string,
  categoryLabel: string,
  isCategory = false,
) => {
  const theme = categoryTheme[categoryId] ?? {
    from: "#f8fafc",
    to: "#e2e8f0",
    accent: "#334155",
  };
  const emoji =
    wordEmoji[`${categoryId}:${title.toLowerCase()}`] ??
    wordEmoji[title] ??
    wordEmoji[title.toLowerCase()] ??
    categoryEmoji[categoryId] ??
    "🔤";
  const safeTitle = escapeSvg(shortLabel(title, 40));
  const safeSubtitle = escapeSvg(shortLabel(subtitle, 22));
  const safeCategory = escapeSvg(shortLabel(categoryLabel, 34));
  const titleSize = fontSizeFor(title, 46);
  const subtitleSize = fontSizeFor(subtitle, 34);

  const svg = `
<svg xmlns="http://www.w3.org/2000/svg" width="640" height="480" viewBox="0 0 640 480">
  <defs>
    <linearGradient id="bg" x1="0" y1="0" x2="1" y2="1">
      <stop offset="0%" stop-color="${theme.from}"/>
      <stop offset="100%" stop-color="${theme.to}"/>
    </linearGradient>
  </defs>
  <rect width="640" height="480" rx="34" fill="url(#bg)"/>
  <circle cx="90" cy="78" r="76" fill="#ffffff" opacity="0.46"/>
  <circle cx="552" cy="396" r="98" fill="#ffffff" opacity="0.34"/>
  <rect x="48" y="42" width="544" height="396" rx="30" fill="#ffffff" opacity="0.74"/>
  <text x="320" y="180" text-anchor="middle" font-size="112" font-family="Apple Color Emoji, Segoe UI Emoji, Noto Color Emoji, sans-serif">${emoji}</text>
  <text x="320" y="288" text-anchor="middle" font-size="${titleSize}" font-weight="800" fill="#172033" font-family="Inter, Arial, sans-serif">${safeTitle}</text>
  <text x="320" y="338" text-anchor="middle" font-size="${subtitleSize}" font-weight="700" fill="${theme.accent}" font-family="Noto Sans SC, Microsoft YaHei, Arial, sans-serif">${safeSubtitle}</text>
  <rect x="145" y="374" width="350" height="46" rx="23" fill="${theme.accent}" opacity="0.12"/>
  <text x="320" y="405" text-anchor="middle" font-size="22" font-weight="700" fill="${theme.accent}" font-family="Noto Sans SC, Microsoft YaHei, Arial, sans-serif">${safeCategory}</text>
</svg>`;

  const fallbackImage = `data:image/svg+xml;charset=UTF-8,${encodeURIComponent(svg)}`;
  const imageSource = configuredImageSourceFor(categoryId, title, isCategory);

  if (imageSource.mode === "wikiPage") {
    return `wiki-photo:${encodeURIComponent(imageSource.pageTitle)}|||${fallbackImage}`;
  }

  if (imageSource.mode === "directUrl") {
    return `direct-photo:${encodeURIComponent(imageSource.url)}|||${fallbackImage}`;
  }

  return fallbackImage;
};

const idSafe = (value: string) =>
  value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");

