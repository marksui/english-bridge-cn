export const vocabulary: VocabularyItem[] = rawCategories.flatMap((category) =>
  category.words.map((item, wordIndex) => {
    const example = buildExample(category, item);
    return {
      id: `${category.id}-${wordIndex + 1}-${idSafe(item.word)}`,
      categoryId: category.id,
      categoryChinese: category.categoryChinese,
      categoryEnglish: category.categoryEnglish,
      word: item.word,
      chinese: item.chinese,
      pronunciationHint: item.hint ?? item.word.toUpperCase(),
      imageUrl: imageUrl(category.id, item.word, item.chinese, category.categoryChinese),
      example: example.example,
      exampleChinese: example.exampleChinese,
    };
  }),
);

export const getCategoryById = (categoryId: string) =>
  categories.find((category) => category.id === categoryId);

export const getWordsByCategory = (categoryId: string) =>
  vocabulary.filter((item) => item.categoryId === categoryId);

